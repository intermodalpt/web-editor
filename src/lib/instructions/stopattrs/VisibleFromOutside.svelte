<h2 id="is_visible_from_outside" class="card-title">
    Visibilidade do veiculo para a paragem<span class="font-mono text-xs"
        >is_visible_from_outside</span
    >
</h2>
<p>
    Um motorista consegue concluir se uma paragem tem passageiros (no exterior) atempadamente o
    suficiente para efetuar paragem.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
    O contexto primário de aplicação deste atributo tende a ser (não exclusivamente) acessos a
    paragens que implicam desvios atempados, os quais poderão não ser feitos por falta de
    visibilidade.
</p>