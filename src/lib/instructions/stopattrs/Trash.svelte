<h2 id="has_trash_can" class="card-title">
	Caixote do lixo <span class="font-mono text-xs">has_trash_can</span>
</h2>
<p>
	É considerada caixote do lixo uma infraestrutura que, estando próxima à paragem, permite aos
	passageiros o descarte de lixo geral.
</p>

<p class="pl-2 border-info border-l-2">
	Caixotes do lixo nas imediações próximas da paragem poderão ser considerados como da paragem,
	desde que a usufruição dos mesmos não prejudique a utilização do serviço (ex. distanciando os
	passageiros de tal forma que inviabilize a solicitação de serviço).
</p>
<p class="pl-2 border-warning border-l-2">
	A existência de reciclagem não é, por si só, suficiente para a atribuição deste atributo. Deverá
	no entanto de ser assinalada, se possível, no OpenStreetMap a fim de estudos futuros.
</p>
