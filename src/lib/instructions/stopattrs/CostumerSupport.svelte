<h2 id="has_costumer_support" class="card-title">
	Apoio ao passageiro <span class="font-mono text-xs">has_costumer_support</span>
</h2>
<p>A existência de um serviço de apoio ao passageiro em pelo menos uma das seguintes categorias:</p>
<ul class="pl-4 list-disc">
	<li>Aquisição e carregamento de títulos de transporte</li>
	<li>Informação navegacional</li>
	<li>Resolução de problemas</li>
</ul>

<p class="pl-2 border-info border-l-2">
	O apoio ao passageiro não necessita de ser da mesma operadora, desde que se insira nas categorias
	anteriores para pelo menos um dos operadores efetuantes de serviço na paragem mencionada.
</p>
<p class="pl-2 border-info border-l-2">
	O apoio ao passageiro, mesmo que efetuado ao vivo por um humano da operadora do serviço a apoiar,
	não carece da realização de todas as categorias.
</p>
<p class="pl-2 border-warning border-l-2">
	Indicação de linhas telefónicas de apoio ao cliente não contam como apoio ao passageiro.
</p>
<p class="pl-2 border-info border-l-2">
	Infraestrutura dedicada, como intercomunicadores, são contabilizados como apoio ao passageiro.
</p>
