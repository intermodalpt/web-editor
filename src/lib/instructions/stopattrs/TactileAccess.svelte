<h2 id="has_tactile_access" class="card-title">
	Acesso táctil <span class="font-mono text-xs">has_tactile_access</span>
</h2>
<p>
	Otimamente, indica que existem indicações para invisuais ao longo de todo o comprimento de acesso
	para pelo menos um dos acessos por segmento de via adjacente à paragem. Estas indicações dizem
	respeito, por exemplo, a atravessamentos de via.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	Sendo de difícil completude, poderá ser aplicado a casos de infraestrutura parcial, onde os
	principais acessos oferecem estas infraestruturas.
</p>
