<h2 id="parking_visibility_impairment" class="card-title">
	Parque automóvel / Limitação visual<span class="font-mono text-xs"
		>parking_visibility_impairment</span
	>
</h2>
<p>
	O parque automóvel <b>expectável</b> impede a visibilidade da paragem (seja da paragem para os serviços
	ou dos serviços para a paragem).
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	Deverá de ser contabilizado estacionamento ilegal, estacionamento noturno (caso existam dados para
	tal) e as “paragens temporárias”, quando recorrentes no local.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	O atributo deverá de ser visto de forma pragmática, sendo aplicado mesmo que o parque automóvel
	esteja a ser respeitar todas as leis em vigor. O critério incide na qualidade que o local oferece
	ao passageiro e não na lei.
</p>

<h3 class="text-lg font-semibold">Exemplos de aplicação</h3>
<p>
	Independentemente dos veículos tipicamente estacionados ou da estatura do passageiro, é improvável
	que o passageiro veja a sua visibilidade afetada <b>pelo parque automóvel</b>. Considerado
	<b>sem limitação</b>.
</p>
<p>
	Paragem lateraliza um estacionamento em paralelo que dificulta a visibilidade num cenário
	conveniente (como estando sentado num abrigo). Considerado <b>pouco limitante</b>.
</p>
<p>
	É comum a existência de carros parados em segunda fila, os quais cortam alguma visibilidade.
	Considerado <b>algo limitante</b>.
</p>
<p>
	Paragem encontra-se recedida sobre um estacionamento perpendicular próximo. O passageiro de baixa
	estatura tem, no pior cenário típico, de se posicionar na via de rodagem para conseguir observar
	veículos. Considerado <b>muito limitante</b>.
</p>
