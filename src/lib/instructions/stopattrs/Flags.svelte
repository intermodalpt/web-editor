
<h2 id="flags" class="card-title">Postaletes <span class="font-mono text-xs">flags</span></h2>
<p>
    Postaletes são os identificadores (geralmente textuais) primários da paragem. Cada postalete
    contem os serviços efetuados no ponto de paragem bem como, tipicamente, um nome e um
    identificador.
</p>
<p>
    Múltiplos postaletes com o mesmo identificador são considerados o mesmo pelo que deverão de
    ser uma entrada única. Postaletes com o mesmo identificador e nomes distintos são um erro
    que deverá de ser reportado ao projeto e à operadora. Em caso de dúvida, poderão ser
    colocados enquanto entradas distintas.
</p>
<p class="pl-2 border-warning border-l-2">
    <b>Não deverá de haver inserção parcial de informação de postaletes.</b><br />
    As inserções deverão de ser plenas (passagem de nada para a totalidade dos postaletes bem como
    totalidade das linhas afetadas).
</p>