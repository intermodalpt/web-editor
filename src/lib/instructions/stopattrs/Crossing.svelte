
<h2 id="has_crossing" class="card-title">
    Atravessamento de via <span class="font-mono text-xs">has_crossing</span>
</h2>
<p>
    Existência de forma <b>segura</b> de atravessamento das vias adjacentes à paragem. Para ser assinalado
    é necessário que exista como alcançar todos os segmentos de via adjacente de forma suficientemente
    segura (ie. uma pessoa de pequena estatura a ritmo lento não corre riscos substanciais) e sem
    grandes transtornos (ex. contornos de centenas de metros).
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
    Enquanto caso notável: A existência de passadeiras, pontes, túneis ou outras infraestruturas que impliquem desvios
    inferiores a 50 metros e permitam o acesso a todos os segmentos de via desejáveis, implicam
    que existe um atravessamento de via.
</p>