<h2 id="has_crossing" class="card-title">
	Atravessamento de via <span class="font-mono text-xs">has_crossing</span>
</h2>
<p>
	Existência de forma <b>segura</b> de atravessamento das vias adjacentes no acesso à paragem. Para ser
	assinalado é necessário que exista como alcançar todos os segmentos de via adjacente de forma suficientemente
	segura (ie. uma pessoa de pequena estatura a ritmo lento não corre riscos substanciais) e sem grandes
	transtornos (ex. contornos de centenas de metros).
</p>

<div class="-ml-2 pl-2 border-info border-l-2">
	<p>
		A existência de uma via adjacente que não oferece uma passadeira não implica que a paragem não
		oferece atravessamento de via. O critério assenta no quão inofensivo e célere é atravessar vias
		no alcance da paragem. Neste sentido certas vias poderão, por si só, qualificar-se enquanto
		atravessáveis sem a existência de infraestrutura de suporte.<br />
	</p>
	<p>
		Alguns exemplos de vias com atravessamento de via implícito são: Algumas estradas de muito boa
		visibilidade e baixo fluxo de tráfego, zonas de coexistência, ciclovias, terminais de autocarro
		ou paragens de metro de superfície.
	</p>
</div>

<img class="h-96" src={Crossing} alt="Exemplo de atravessamentos a ser considerados" />

<p class="-ml-2 pl-2 border-info border-l-2">
	Enquanto caso notável: A existência de passadeiras, pontes, túneis ou outras infraestruturas que
	impliquem desvios inferiores a 50 metros e permitam o acesso a todos os segmentos de via
	desejáveis, implicam que existe um atravessamento de via.
</p>
