<h2 id="parking_local_access_impairment" class="card-title">
	Parque automóvel / Disfuncional à paragem<span class="font-mono text-xs"
		>parking_local_access_impairment</span
	>
</h2>
<p>
	O parque automóvel <b>expectável</b> impede a utilização da paragem alem de questões visuais, por exemplo,
	bloqueando o acesso à paragem, bloqueando o acesso aos veículos, colocando situações de risco aos utentes,
	ou criando outras situações adversas ao bom funcionamento da paragem.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	Deverá de ser contabilizado estacionamento ilegal, estacionamento noturno (caso existam dados para
	tal) e as “paragens temporárias”, quando recorrentes no local.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	O atributo deverá de ser visto de forma pragmática, sendo aplicado mesmo que o parque automóvel
	esteja a ser respeitar todas as leis em vigor. O critério incide na qualidade que o local oferece
	ao passageiro e não na lei.
</p>
<h3 class="text-lg font-semibold">Exemplos de aplicação</h3>
<p>
	É passageiro veja o espaço útil da paragem afetado <b>pelo parque automóvel</b>. Considerado
	<b>sem limitação</b>.
</p>
<p>
	Os veículos tem de efetuar uma paragem distante devido aos obstáculos do parque automóvel. Não
	existem barreiras nem ressaltos tratando-se apenas de uma leve inconveniência ao mais frágil dos
	passageiros. Considerado <b>pouco limitante</b>.
</p>
<p>
	O parque automóvel invade recorrentemente o espaço da paragem, limitando a área útil aos
	passageiros ou comprometendo a facilidade de acesso a uma cadeira de rodas. Considerado <b
		>algo limitante</b
	>.
</p>
<p>
	O passageiro tem de penetrar ou contornar carros estacionados para alcançar os veículos.
	Considerado <b>muito limitante</b>.
</p>
