<h2 id="has_illuminated_path" class="card-title">
	Iluminação no acesso<span class="font-mono text-xs">has_illuminated_path</span>
</h2>
<span class="text-xs">Referente à visibilidade noturna.</span>
<p>
	A existência de iluminação de intensidade média ou superior para pelo menos um dos acessos por
	segmento de via adjacente à paragem.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	Este atributo pode considerar fontes de iluminação fundidas, dado que as mesmas tipicamente
	operam.
</p>
<p class="-ml-2 pl-2 border-error border-l-2">
	O atributo de "<i>Iluminação fundida</i>" não deve de ser utilizado para iluminações no acesso,
	apenas para a primária da paragem.
</p>
