<h2 id="has_bench" class="card-title">Banco <span class="font-mono text-xs">has_bench</span></h2>
<p>
	São considerados como banco as infraestruturas <b>dedicadas</b> ao repouso <b>sentado</b> dos passageiros.
</p>

<p class="pl-2 border-info border-l-2">
	Bancos nas imediações próximas da paragem poderão ser considerados como bancos da paragem, desde
	que a usufruição dos mesmos não prejudique a utilização do serviço (ex. distanciando os
	passageiros de tal forma que inviabilize a solicitação de serviço).
</p>
<p class="pl-2 border-warning border-l-2">Encostos e muros não são considerados bancos.</p>
