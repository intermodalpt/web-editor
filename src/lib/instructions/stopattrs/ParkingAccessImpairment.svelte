<h2 id="parking_area_access_impairment" class="card-title">
	Parque automóvel / Disfuncional ao acesso<span class="font-mono text-xs"
		>parking_area_access_impairment</span
	>
</h2>
<p>
	O parque automóvel <b>expectável</b> prejudica o acesso à paragem, seja em matéria de conveniência,
	segurança ou acessibilidade.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	Deverá de ser contabilizado estacionamento ilegal, estacionamento noturno (caso existam dados para
	tal) e as “paragens temporárias”, quando recorrentes no local.
</p>
<p class="-ml-2 pl-2 border-info border-l-2">
	O atributo deverá de ser visto de forma pragmática, sendo aplicado mesmo que o parque automóvel
	esteja a ser respeitar todas as leis em vigor. O critério incide na qualidade que o acesso oferece
	ao passageiro e não na lei.
</p>

<h3 class="text-lg font-semibold">Exemplos de aplicação</h3>
<p>
	O passageiro nunca vê o seu melhor acesso limitado <b>pelo parque automóvel</b>. Considerado
	<b>sem limitação</b>.
</p>
<p>
	O passageiro pode ter de alternar o lado da rua em que se encontra afim de contornar o parque
	automóvel. Considerado <b>pouco limitante</b>.
</p>
<p>
	O parque automóvel causa um estreitamento na generalidade dos acessos. Considerado <b
		>algo limitante</b
	>.
</p>
<p>
	O passageiro pode ter caminhar pela estrada por impedimento do parque automóvel. Considerado <b
		>muito limitante</b
	>.
</p>
