<h2 id="issues" class="card-title">
	Defeitos <span class="font-mono text-xs">tmp_issues</span>
</h2>
<p>
	Defeitos são imperfeições <b>temporárias</b>. Os seguintes defeitos podem ser listados neste
	atributo e devem de ser complementados de uma nota descritiva do problema:
</p>
<div class="ml-2">
	<h3 class="text-md font-semibold">
		Luz fundida <span class="font-mono text-xs ml-1">lights_broken</span>
	</h3>
	<p class="ml-1">
		A fonte primária de iluminação encontra-se fundida.<br />
		Ausência de luz primária implica que a paragem perdeu mais de metade da sua iluminação bruta (ie.
		ficou substancialmente mais escura).
	</p>
	<h3 class="text-md font-semibold">
		Acesso fundido <span class="font-mono text-xs ml-1">path_lights_broken</span>
	</h3>
	<p class="ml-1">Existem fontes primárias da iluminação do acesso fundidas</p>
	<h3 class="text-md font-semibold">
		Dano leve <span class="font-mono text-xs ml-1">damage_low</span>
	</h3>
	<p class="ml-1">Vandalismo cosmético ou estragos de baixo impacto na usabilidade.</p>
	<h3 class="text-md font-semibold">
		Dano moderado <span class="font-mono text-xs ml-1">damage_medium</span>
	</h3>
	<p class="ml-1">
		Danos de impacto moderado na usabilidade ou com impacto na usabilidade dos passageiros, tais
		como: Um postalete partido, horários rasgados ou pintados por cima, um painel lateral em falta
		num abrigo, ou outros cenários análogos.
	</p>
	<h3 class="text-md font-semibold">
		Dano grave <span class="font-mono text-xs ml-1">damage_high</span>
	</h3>
	<p class="ml-1">
		Danos de grande impacto na usabilidade ou com impacto na segurança dos passageiros, tais como:
		Painéis partidos em abrigos, bancos inutilizados, riscos à integridade física dos passageiros e
		situações análogas.
	</p>
	<h3 class="text-md font-semibold">
		Limpeza insuficiente <span class="font-mono text-xs ml-1">dirty</span>
	</h3>
	<p class="ml-1">
		Indícios de falta de limpeza. Sujidade forte tal que seja pouco higiénica aos passageiros ou
		prejudique a funcionalidade da paragem.
	</p>
	<h3 class="text-md font-semibold">
		Recolha de lixo insuficiente <span class="font-mono text-xs ml-1">thrash</span>
	</h3>
	<p class="ml-1">
		O lixo é recolhido com uma frequência de tal forma insuficiente que os caixotes se encontram
		recorrentemente cheios e os chão apresenta lixo resultante.
	</p>
	<h3 class="text-md font-semibold">
		Vegetação excessiva <span class="font-mono text-xs ml-1">weeds</span>
	</h3>
	<p class="ml-1">
		Se existe mata ou outras plantas em proporções que afetem o funcionamento da paragem ou dos seus
		acessos.
	</p>
	<h3 class="text-md font-semibold">
		Informação obsoleta <span class="font-mono text-xs ml-1">obsolete</span>
	</h3>
	<p class="ml-1">
		Se a paragem tem postaletes que já não se aplicam, horários antigos, avisos que já não vigoram
		ou outras informações que podem induzir os passageiros em erro.
	</p>
	<h3 class="text-md font-semibold">
		Obras <span class="font-mono text-xs ml-1">construction_works</span>
	</h3>
	<p class="ml-1">Se a paragem ou o acesso à mesma se encontra obstruído por obras.</p>
</div>
