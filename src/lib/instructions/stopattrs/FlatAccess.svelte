<h2 id="has_flat_access" class="card-title">
	Acesso sem ressaltos <span class="font-mono text-xs">has_flat_access</span>
</h2>
<p>
	A inexistência de degraus (superiores a 1-2cm) ou grandes declives (mais de 6%) para pelo menos um
	dos acessos por segmento de via adjacente à paragem.
</p>
<p class="-ml-2 pl-2 border-warning border-l-2">
	É contabilizado neste atributo o percurso entre a paragem e a entrada num veiculo sempre que o
	veiculo não consiga dispor de uma rampa de acesso nem disponibilizar um acesso plano.
</p>
